# ===== Pyhton while loop ===== #
"""
- while loop merupakan pengulangan yang dilakukan berdasarkan suatu kondisi yang bernilai true
- jika kondisi selalu bernilai true maka looping tidak akan pernah selesai
"""
counter = 1
while counter <= 10:
  print(f"Hello world {counter}")
  counter += 1 # increment counter